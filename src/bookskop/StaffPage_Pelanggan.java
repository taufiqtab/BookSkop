    private void dataTable(){
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("ID");
        tabel.addColumn("nama");
        tabel.addColumn("nama_bioskop");
        tabel.addColumn("nomor_studio");
        tabel.addColumn("judul"); 
        tabel.addColumn("waktu_pemesanan"); 
        tabel.addColumn("Tanggal"); 
        tabel.addColumn("Waktu Mulai"); 
        tabel.addColumn("Waktu Selesai"); 
        tabel.addColumn("baris");
        tabel.addColumn("nomor_bangku"); 
        tabel.addColumn("harga"); 
        
        table_pelanggan.setModel(tabel);
        
        try{
            koneksi();
            String sql="SELECT * FROM pelanggan";
            res=stat.executeQuery(sql);
            
            while (res.next())
            {
                tabel.addRow(new Object[]{
                    res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4),
                    res.getString(5),
                    res.getString(6),
                    res.getString(7),
                    res.getString(8),
                    res.getString(9),
                    res.getString(10),
                    res.getString(11),
                    res.getString(12),
                });
            }
        }catch (SQLException e){
        }
    }

